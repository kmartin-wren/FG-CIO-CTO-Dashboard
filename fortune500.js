function normalizeName(name) {
  return (name || '').toLowerCase()
    .replace(/[,\.®™]/g, '')
    .replace(/\s+/g, ' ')
    .trim();
}

function getFortunRank(companyName) {
  const norm = normalizeName(companyName);
  if (!norm) return null;

  // Direct match
  if (FORTUNE500[norm]) return FORTUNE500[norm];

  // Partial match — check if any key is contained in the company name or vice versa
  for (const [key, rank] of Object.entries(FORTUNE500)) {
    if (key.length < 4) continue; // skip short keys like "gm", "bp"
    if (norm.includes(key) || key.includes(norm)) return rank;
  }

  return null;
}

